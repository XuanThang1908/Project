package com.dxt.monhoc;

public class ChuanDauRa {
    private String chuanDauRa;
    private String moTa;

    public ChuanDauRa() {
    }

    public ChuanDauRa(String chuanDauRa, String moTa) {
        this.chuanDauRa = chuanDauRa;
        this.moTa = moTa;
    }

    public String getChuanDauRa() {
        return chuanDauRa;
    }

    public void setChuanDauRa(String chuanDauRa) {
        this.chuanDauRa = chuanDauRa;
    }

    public String getMoTa() {
        return moTa;
    }

    public void setMoTa(String moTa) {
        this.moTa = moTa;
    }
}

